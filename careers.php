   $(document).ready(function(){
        $(window).scroll(function(){
            if($(window).scrollTop()>=482){
                $(".toTop").fadeIn(1500);
            }else{
                $(".toTop").fadeOut(1500);
            }
						if($(window).width()>1024){
							
								if($(window).scrollTop()>=130){
										
									$(".il_top_h").addClass("visib");
									
									$(".header1").addClass("il_0");
									
									
								}else{
						
									$(".il_top_h").removeClass("visibl");
									
									$(".header1").removeClass("il_0");
								}
								
						
								
							}
			
			$('.parallax-window').parallax({imageSrc: 'images/parallax1.jpg'});
			$('.parallax-window').parallax({imageSrc: 'images/parallax2.jpg'});
	
	
	
			if($(window).scrollTop()>=1600){
				$(".il_careers_content").addClass("op_op")
			}
			
	
	
			if($(window).scrollTop()>=2200){
				$(".il_all_story ").addClass("op_op")
			}
			
			
			
			
			
			
			
			
		
			
			if (window.addEventListener) window.addEventListener('DOMMouseScroll', wheel, false);
				window.onmousewheel = document.onmousewheel = wheel;

				function wheel(event) {
					var delta = 0;
					if (event.wheelDelta) delta = event.wheelDelta / 120;
					else if (event.detail) delta = -event.detail / 3;

					handle(delta);
					if (event.preventDefault) event.preventDefault();
					event.returnValue = false;
				}

				function handle(delta) {
					var time = 100;
					var distance = 200;

					$('html, body').stop().animate({
						scrollTop: $(window).scrollTop() - (distance * delta)
					}, time );
				}
			
			
		
			
			
			
		
			})
			
			
			
		
		
        $(".toTop").click(function(){
            $("body").animate({scrollTop:0},1000)
        })
		
		$(".il_search_form_toggle").click(function(){
			$(".il_search_label").fadeToggle();
			$(".search-form_toggle:before").css({
				"content":""
			})
		})
		

    })
								
